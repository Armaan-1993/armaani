// 63. Write a function that returns the largest element in a list.
const largestElement = (elements) => {
  let output = 0;
  for (let i = 0; i <= output; i++) {
    if (elements[i] > output) {
      output = elements[i];
    }
  }
  return output;
}
  
console.log(largestElement('15485632458'));

/*Output:

8*/
  